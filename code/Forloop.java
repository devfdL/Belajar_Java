public class Forloop
{
    public static void main(String args[]){
        int i;
        for(i=1; i <= 5; i++){
            System.out.println("Java " + i);
        }
        
        System.out.println("--------------");
        
        for(i=5; i >= 1; i--){
            System.out.println("Java " + i);
        } 
    }
}